package ex_09_For_Loop;

public class Lab047_For_Loop {
    public static void main(String[] args) {
        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation(Incre/decre)
        // ICU
        for (int i=0; i<10; i++){
            System.out.println(i);
        }
    }
}
