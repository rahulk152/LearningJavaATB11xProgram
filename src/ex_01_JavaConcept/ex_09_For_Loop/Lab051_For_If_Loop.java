package ex_01_JavaConcept.ex_09_For_Loop;

public class Lab051_For_If_Loop {
    public static void main(String[] args) {
        for(int i = 0; i < 18; i++){
            if(i > 15){
                System.out.println("You will get gift of iPhone");
            }
            else{
                System.out.println("No gift");
            }
        }
    }
}
