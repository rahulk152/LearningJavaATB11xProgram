package ex_SDET_Coding_Questions.April_8_2025.WhileLoop;

public class Task27_Print_Table_Of_7 {
    public static void main(String[] args) {
        int table = 7;
        int i = 1;
        while (i <= 10){
            System.out.println(table+ " * " +i+ " = " +(table*i));
            i++;
        }
    }
}
