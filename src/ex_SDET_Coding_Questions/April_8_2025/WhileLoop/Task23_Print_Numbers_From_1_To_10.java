package ex_SDET_Coding_Questions.April_8_2025.WhileLoop;

public class Task23_Print_Numbers_From_1_To_10 {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("Print Number 1 To 10 : ");
        while (i<=10){
            System.out.println(+i);
            i++;
        }
    }
}
