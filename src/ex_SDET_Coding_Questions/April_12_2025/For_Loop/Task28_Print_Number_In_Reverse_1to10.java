package ex_SDET_Coding_Questions.April_12_2025.For_Loop;

public class Task28_Print_Number_In_Reverse_1to10 {
    public static void main(String[] args) {
        System.out.println("The Number 10 to 1 in Reverse Order : ");
        for (int i = 10; i >=1 ; i--) {
            System.out.println(i);
        }
    }
}
